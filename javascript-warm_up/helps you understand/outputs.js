let a = "Asap";
let b = "Rocky";

// a) USE OF (,)    //a comma gives a default space between
console.log("My name is",a,b);

// b) USE OF (+)    // a + doesn't give a default space between thats why we put double quotes for spacing
console.log("My name is "+a+" "+b);

// c) USE OF ($)    // a $ is custom. It gives a spacing if you put one during the output
console.log(`My name is ${a} ${b}`);




